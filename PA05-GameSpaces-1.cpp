#include "PlanetX.h"
#include "Spaceship.h"

// entry point
//------------------------------------------------------------------------------
int main()
{
    /*******************************************************************************
        * Initialize classes needed
    /******************************************************************************/
    Spaceship ship;
    PlanetX px;

    /*******************************************************************************
        * UX intro
    /******************************************************************************/
    
    ship.print("Opening Info");
    px.print("Opening Info");
    
    ship.print();
    px.print();

    ship.departureStatus();
    
    /*******************************************************************************
        * Orbit and rescue -- ship
    /******************************************************************************/
    ship.travelTo(px);
    ship.rescue(px); 
    ship.print("Rescue Complete!");
    
    /*******************************************************************************
        * display new planet statuses
    /******************************************************************************/
    px.print("Planet Status");
    px.print();

    /*******************************************************************************
       * updated ship right before orbit
    /******************************************************************************/
    ship.print();

    /*******************************************************************************
       * leave and tell da story
    /******************************************************************************/
    ship.leaveOrbit(px);
    px.print("Planet Status");
    ship.resolutionStatus();
    ship.print("Finale!");
    
}
